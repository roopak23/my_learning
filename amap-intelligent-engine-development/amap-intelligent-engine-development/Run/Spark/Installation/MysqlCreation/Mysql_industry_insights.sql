CREATE TABLE IF NOT EXISTS industry_insights (
  id VARCHAR(255) NOT NULL,
  date VARCHAR(255) DEFAULT NULL,
  industry VARCHAR(255) DEFAULT NULL ,
  channel VARCHAR(255) DEFAULT NULL,
  media_type VARCHAR(255) DEFAULT NULL,
  report_id VARCHAR(255) DEFAULT NULL,
  cpc DOUBLE DEFAULT 0.0,
  ctr DOUBLE DEFAULT 0.0,
  cpcv DOUBLE DEFAULT 0.0,
  cpv DOUBLE DEFAULT 0.0,
  cpm DOUBLE DEFAULT 0.0,
  view_rate DOUBLE DEFAULT 0.0,
  impressions INTEGER DEFAULT 0,
  player_25 INTEGER DEFAULT 0,
  player_50 INTEGER DEFAULT 0,
  player_75 INTEGER DEFAULT 0,
  player_100 INTEGER DEFAULT 0,
  post_engagement INTEGER DEFAULT 0,
  time_of_day VARCHAR(255) DEFAULT NULL,
  day_of_week VARCHAR(255) DEFAULT NULL,
  device VARCHAR(255) DEFAULT NULL,
  gender VARCHAR(255) DEFAULT NULL,
  age VARCHAR(255) DEFAULT NULL,
  keyword VARCHAR(255) DEFAULT NULL,
  display_format VARCHAR(255) DEFAULT NULL,
  social_placement VARCHAR(255) DEFAULT NULL,
  network_type VARCHAR(255) DEFAULT NULL,
  country VARCHAR(255) DEFAULT NULL,
  state VARCHAR(255) DEFAULT NULL,
  region VARCHAR(255) DEFAULT NULL,
  city VARCHAR(255) DEFAULT NULL,
  email_total_sent DOUBLE DEFAULT 0.0,
  unique_open_rate DOUBLE DEFAULT 0.0,
  unique_click_rate DOUBLE DEFAULT 0.0,
  sms_total_sent DOUBLE DEFAULT 0.0,
  PRIMARY KEY (id)
);